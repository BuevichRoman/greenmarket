-- Добавлена задним числом при первом реальном деплое на aristotel_taxi (сервер 104.171.133.95):
-- 004 (исходная нумерация, сейчас 006_create_seller_product_photos.sql) ссылалась на таблицу
-- Photo, которой не существовало ни в одной среде. Временное решение (Stage 1): метаданные
-- фотографий, сами файлы хранятся в S3, таблица хранит только ключ объекта.

/*
|--------------------------------------------------------------------------
| GreenMarket
|--------------------------------------------------------------------------
| Migration : 004_create_photo.sql
| Purpose   : Создание таблицы метаданных фотографий (файлы — в S3)
| DBMS      : MySQL Community Server 8.0.16+
|--------------------------------------------------------------------------
*/

CREATE TABLE Photo
(
    id          BIGINT UNSIGNED NOT NULL AUTO_INCREMENT
        COMMENT 'Первичный ключ',

    s3_key      VARCHAR(500) NOT NULL
        COMMENT 'Ключ объекта в S3-хранилище (сам файл в БД не хранится)',

    created_at  DATETIME NOT NULL
        DEFAULT CURRENT_TIMESTAMP
        COMMENT 'Дата создания записи (UTC)',

    PRIMARY KEY (id),

    UNIQUE INDEX uk_Photo_s3_key (s3_key)
)
ENGINE = InnoDB
DEFAULT CHARSET = utf8mb4
COLLATE = utf8mb4_0900_ai_ci
COMMENT = 'Метаданные фотографий (временное решение Stage 1, файлы в S3)';

/*
|--------------------------------------------------------------------------
| Архитектурные решения
|--------------------------------------------------------------------------
| 1. Photo хранит только ссылку на объект в S3 (s3_key) — бинарные данные
|    в БД не хранятся.
| 2. Временное решение Stage 1: если позже появится отдельный платформенный
|    медиа-сервис — таблица может быть заменена без изменения контракта
|    fk_SellerProductPhoto_photo (006_create_seller_product_photos.sql),
|    смотрящего на Photo(id).
| 3. UNIQUE(s3_key) — один объект S3 не может быть зарегистрирован дважды.
| 4. Физическое удаление Photo не запрещено на уровне схемы (в отличие от
|    бизнес-сущностей каталога) — очистка неиспользуемых фото выполняется
|    отдельным сервисом обслуживания (см. 006_create_seller_product_photos.sql,
|    п.5 архитектурных решений).
|--------------------------------------------------------------------------
*/
