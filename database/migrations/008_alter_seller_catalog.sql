ALTER TABLE Seller
    ADD COLUMN current_catalog_version  INT UNSIGNED NULL
        COMMENT 'Версия текущего рабочего каталога',
    ADD COLUMN current_publication_key  CHAR(36) NULL
        COMMENT 'PublicationKey текущего рабочего каталога',
    ADD COLUMN current_catalog_hash     CHAR(64) NULL
        COMMENT 'SHA-256 текущего рабочего каталога',
    ADD UNIQUE INDEX uk_Seller_publication_key (current_publication_key);

