def _build_group_rows() -> list[list[object]]:
    rows: list[list[object]] = []
    id_by_name: dict[str, int] = {}
    for next_id, (name, parent_name) in enumerate(_GROUP_DEFS, start=1):
        parent_id = id_by_name[parent_name] if parent_name else None
        id_by_name[name] = next_id
        rows.append([next_id, parent_id, name])
    return rows


def _build_product_rows(group_rows: list[list[object]]) -> list[list[object]]:
    group_id_by_name = {row[2]: row[0] for row in group_rows}
    return [
        [product_id, group_id_by_name[group_name], name]
        for product_id, (name, group_name) in enumerate(_PRODUCT_DEFS, start=1)
    ]


GROUP_ROWS = _build_group_rows()
PRODUCT_ROWS = _build_product_rows(GROUP_ROWS)

